package day05_arithmetic;

public class MathPractice {
    public static void main(String[] args) {
        System.out.println(4+5);
        System.out.println(4*5);
        //int * int
        System.out.println(2*5);

        //int*double --> double
        System.out.println(2 * 5.5);

        //int / int -> int
        System.out.println(10/3);

        //int / double -> double
        System.out.println(10/3.0);

        System.out.println(5%2); // remainder of 1
        System.out.println(4%2); // remainder is 0 because this is evenly divisible
        System.out.println(4 % 10); // remainder is 4 because you cant put 10 into 4









    }
}
